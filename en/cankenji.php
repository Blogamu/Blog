<h3>Can Kenji</h3>
<p>Attracted by the referential Catalan cuisine and famous cooks, in 2003 Kenji Ueno arrives in Barcelona from his hometown Kyoto. Just landed, Kenji joins Satoru Miyano, and from his time spent in the restaurant with the same name, which was a mix between Japanese, French and Mediterranean cuisine, he digs deeper into the author creative cuisine.</p>
<p>After this early contact, Kenji goes on expanding his experience in many influential Japanese restaurants in Barcelona. Meanwhile in 2006, along with his wife Neus, he launches the company Izayaka catering, where his own vision of japanese cuisine gets realized.</p>
<p>It was then when he first conceived the idea of settling in his own restaurant where he could develop his previous experiences as a cook as well as his own vision of a Japanese restaurant.</p>
<p>Can Kenji opened doors in 2010, a restaurant inspired in Japanese traditional taverns known as Izayakas where different courses are shared amongst the guests.</p>
<p>Can Kenji, following traditional Izayaka, serves Japanese food with influences of author cuisine inspired by gastronomic fusion and conditioned by local and seasonal products. <br />
You can eat traditional Japanese courses, which may be difficult to find in menus of other Japanese restaurants in town, as well as a selection of conventional Japanese and Mediterranean reinterpreted dishes.</p>
<p>Can Kenji is a quiet and small restaurant where time seems to stop for enjoying the stay, where the order in which the courses are eaten is less important and where the experience of eating becomes an original degustation of tapas that leaves no one indifferent.</p>
<p>In 2015 Kenji and Neus opened <a href="http://aiueno.com/" target="_blank" class="inner-link">Aiueno</a>, Can Kenji&#8217;s middle sibling and by the end of 2017 the family grew with the opening of a new restaurant: <a href="http://satotanaka.com/" target="_blank" class="inner-link">Sato i Tanaka</a>.</p>
