<h3>Wine and Drinks</h3>
<h5>The cellar at Can Kenji puts together a collection of original and daring wines with lots of personality, some of them are made in limited numbers for local consumption and produced at the cellars of familiar properties.<br />
You can also try the sake or the sochu and the japanese cocktails.</h5>
<h3>The daily menu</h3>
<h5>The daily menu is on from monday to friday at dinner time . Choose between a fish or meat main course, along with a bowl of plain rice and a miso soup. To try the tasting menu, choose within 3 or 4 of the dishes listed and combine it with a delicious portion of sushi.</h5>
<h3>Bento &#038; catering</h3>
