<h3>Can Kenji</h3>
<p>L&#8217;any 2003 provinent de Kyoto, la seva ciutat natal, el Kenji Ueno aterra a Barcelona atret pels referents culinaris catalans del moment. Acabat d&#8217;arribar entra a treballar amb Satoru Miyano i, del seu pas pel desaparegut restaurant amb el mateix nom, que fusionava la gastronomia japonesa, francesa i mediterrània, s&#8217;endinsa en el concepte de la cuina creativa d&#8217;autor.</p>
<p>Desprès d&#8217;aquest contacte inicial, el Kenji continua aprofundint en la seva experiència en diverses cuines de restaurants japonesos de referència a la ciutat. L’any 2006 funda l&#8217;empresa Izakaya Catering juntament amb la seva dona, la Neus Busquets, on comença a portar a terme la seva particular visió de la cuina japonesa.</p>
<p>És a partir d&#8217;aquest moment quan en el seu cap es comença a coure la idea de buscar el seu propi espai, un lloc on poder plasmar les seves experiències a la cuina i la seva proposta de com ha de ser un restaurant.</p>
<p>L&#8217;any 2010 s&#8217;inaugura Can Kenji, un restaurant inspirat en les tavernes tradicionals japoneses, les izakayes. La manera de menjar en una izakaya consisteix en demanar diferents plats per compartir entre els comensals.</p>
<p>Així doncs, aquesta izakaya  d&#8217;estil culinari tradicional japonès influenciada per la cuina d&#8217;autor, per la fusió gastronòmica i deliciosament condicionada per els productes de consum local i de temporada. S’hi poden menjar plats tradicionals que rarament formen part de les cartes d&#8217;altres restaurants japonesos a occident i també reinterpretacions de plats més convencionals, de les gastronomies japonesa i mediterrània.</p>
<p>El restaurant és petit i tranquil. Es diria que algú ha decidit ralentitzar el temps per a poder gaudir de l&#8217;estada sense presses, on l’ordre en què es demanen i es consumeixen els plats deixen de tenir importància i, on l&#8217;experiència de menjar, es converteix en una original desgustació de tapes que no ens deixarà indiferent.</p>
<p>El 2015 Kenji i Neus obren <a href="http://aiueno.com/" target="_blank" class="inner-link">Aiueno</a>, el germà mitjà de Can Kenji i a finals de 2017 amplíen la família obrint un altre restaurant: <a href="http://satotanaka.com/" target="_blank" class="inner-link">Sato i Tanaka</a>.</p>
