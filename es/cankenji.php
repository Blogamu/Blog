<h3>Can Kenji</h3>
<p>En el año 2003 proveniente de Kyoto, su ciudad natal, Kenji Ueno aterriza en Barcelona atraído por los referentes culinarios catalanes del momento. Acabado de llegar entra a trabajar con Satoru Miyano y, de su paso por el desaparecido restaurante con el mismo nombre, que fusionaba la gastronomía japonesa, francesa y mediterránea, se adentra en el concepto de la cocina creativa de autor.</p>
<p>Desprendido de este contacto inicial, Kenji continúa profundizando en su experiencia en varias cocinas de restaurantes japoneses de referencia a la ciudad. El año 2006 funda la empresa Izakaya Catering junto con su mujer, Neus Busquets, donde empieza a llevar a cabo su particular visión de la cocina japonesa.</p>
<p>Es a partir de este momento cuando en su cabeza se empieza a cocer la idea de buscar su propio espacio, un lugar donde poder plasmar sus experiencias en la cocina y su propuesta de cómo tiene que ser un restaurante.</p>
<p>El año 2010 se inaugura Can Kenji, un restaurante inspirado en las tascas tradicionales japonesas, las Izakayas. La manera de comer en una Izayaka consiste en pedir diferentes platos para compartir entre los comensales.</p>
<p>Así pues, nace esta Izayaka de estilo culinario tradicional japonés influenciada por la cocina de autor, por la fusión gastronómica y deliciosamente condicionada por los productos de consumo local y de temporada. Se pueden comer platos tradicionales que raramente forman parte de las cartas otros restaurantes japoneses en occidente y también reinterpretaciones de platos más convencionales, de las gastronomías japonesa y mediterránea.</p>
<p>El restaurante es pequeño y tranquilo. Se diría que alguien ha decidido ralentizar el tiempo para poder disfrutar de la estancia sin prisas, donde la orden en que se piden y se consumen los platos dejan de tener importancia y, donde la experiencia de comer, se convierte en una original degustación de tapas que no nos dejará indiferente.</p>
<p>En 2015 Kenji y Neus abren <a href="http://aiueno.com/" target="_blank" class="inner-link">Aiueno</a>, el hermano mediano de Can Kenji y a finales de 2017 amplían la familia con la apertura de otro restaurante: <a href="http://satotanaka.com/" target="_blank" class="inner-link">Sato i Tanaka</a>.</p>
